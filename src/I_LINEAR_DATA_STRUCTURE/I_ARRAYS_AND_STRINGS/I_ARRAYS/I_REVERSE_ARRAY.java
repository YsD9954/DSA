package I_LINEAR_DATA_STRUCTURE.I_ARRAYS_AND_STRINGS.I_ARRAYS;
public class I_REVERSE_ARRAY {
    public static void main(String[] args) {
        int arr[] = {1, 2, 3, 4, 5};
        for (int i=arr.length-1;i>=0;i--){
            System.out.print(arr[i]+" ");
        }

    }
}



